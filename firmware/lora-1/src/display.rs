//! SH1106 128×64 OLED driver for STM32WL55 / embassy-stm32.
//!
//! Hardware constraints discovered by testing:
//!   - The display NACKs I2C data writes longer than ~9 bytes (1 control + 8 data).
//!   - After any NACK the I2C peripheral locks up and must be re-created.
//!   - Multi-byte SH1106 commands must be in a single I2C transaction.
//!   - SH1106 has a +2 column hardware offset (128 visible columns start at internal col 2).
//!
//! Design: direct write — no framebuffer.
//!   draw_text() writes each character straight to the display RAM.
//!   clear() writes blank columns page-by-page.
//!   No flush() needed.

use embassy_stm32::{
    i2c::{Config as I2cConfig, I2c},
    peripherals::{I2C2, PA11, PA12},
    time::Hertz,
};

const ADDR: u8 = 0x3C;
const COL_OFFSET: u8 = 2; // SH1106 internal column offset

