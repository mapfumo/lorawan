/// Create a fresh I2C instance. Must be called to recover after any failed write.
fn new_i2c() -> I2c<'static, embassy_stm32::mode::Blocking> {
    let mut cfg = I2cConfig::default();
    cfg.sda_pullup = true;
    cfg.scl_pullup = true;
    // SAFETY: single-threaded, peripheral stolen and immediately released in each call site.
    unsafe {
        I2c::new_blocking(I2C2::steal(), PA12::steal(), PA11::steal(), Hertz(100_000), cfg)
    }
}

/// Send a command (or multi-byte command) to the SH1106.
/// Creates a fresh I2C instance each call to avoid bus lockup from cumulative byte count.
fn send_cmd(bytes: &[u8]) {
    let mut buf = [0u8; 4]; // control byte + up to 3 command bytes
    buf[0] = 0x00; // Co=0, D/C=0 = command stream
    buf[1..1 + bytes.len()].copy_from_slice(bytes);
    let mut i2c = new_i2c();
    let _ = i2c.blocking_write(ADDR, &buf[..1 + bytes.len()]);
}

/// Set the page (row) and column for the next write.
fn set_pos(page: u8, col: u8) {
    let c = col + COL_OFFSET;
    send_cmd(&[0xB0 | page]);       // page address
    send_cmd(&[c & 0x0F]);           // column low nibble
    send_cmd(&[0x10 | (c >> 4)]);   // column high nibble
}

/// Write up to 8 data bytes directly to the display RAM at the current cursor position.
fn write_data(data: &[u8]) {
    let mut buf = [0u8; 9]; // 1 control + up to 8 data bytes
    buf[0] = 0x40; // D/C=1 = data
    buf[1..1 + data.len()].copy_from_slice(data);
    let mut i2c = new_i2c();
    let _ = i2c.blocking_write(ADDR, &buf[..1 + data.len()]);
}

/// Initialise the SH1106. Call once after power-on.
pub fn init() {
    send_cmd(&[0xAE]);         // display off
    send_cmd(&[0xD5, 0x80]);   // clock divide ratio / oscillator frequency
    send_cmd(&[0xA8, 0x3F]);   // multiplex ratio = 64
    send_cmd(&[0xD3, 0x00]);   // display offset = 0
    send_cmd(&[0x40]);         // display start line = 0
    send_cmd(&[0xAD, 0x8B]);   // internal DC-DC on (SH1106-specific)
    send_cmd(&[0xA1]);         // segment remap: col 131 → SEG0
    send_cmd(&[0xC8]);         // COM output scan direction: reversed
    send_cmd(&[0xDA, 0x12]);   // COM pins hardware config
    send_cmd(&[0x81, 0xCF]);   // contrast
    send_cmd(&[0xD9, 0xF1]);   // pre-charge period
    send_cmd(&[0xDB, 0x40]);   // VCOMH deselect level
    send_cmd(&[0xA4]);         // output follows RAM content
    send_cmd(&[0xA6]);         // normal display (not inverted)
    send_cmd(&[0xAF]);         // display on
}

/// Clear the entire display (all 8 pages, 128 columns).
pub fn clear() {
    for page in 0u8..8 {
        // Write 128 zero bytes as 16 × 8-byte chunks.
        for seg in 0u8..16 {
            set_pos(page, seg * 8);
            write_data(&[0, 0, 0, 0, 0, 0, 0, 0]);
        }
    }
}

/// Draw a text string at page row (0–7), starting at pixel column (0–127).
/// Each character is 6 pixels wide (5 glyph columns + 1 gap).
/// Characters beyond the 128-column boundary are silently dropped.
pub fn draw_text(page: u8, col: u8, text: &str) {
    let mut x = col;
    for ch in text.bytes() {
        if x as usize + 6 > 128 { break; }
        let idx = ch.saturating_sub(0x20) as usize;
        let g = if idx < 95 { FONT[idx] } else { FONT[0] };
        // 5 glyph bytes + 1 spacing byte = 6 bytes, well within the 8-byte data limit
        set_pos(page, x);
        write_data(&[g[0], g[1], g[2], g[3], g[4], 0x00]);
        x += 6;
    }
}
